constexpr auto VERTEX_COUNT_MAX_WIRE = 300;

///////////////////////////////////////////////////////////

struct VertexWire
{
    com::Vec2f pos;
    com::Col4n col;
};

///////////////////////////////////////////////////////////

struct PushConstantsWire
{
    i32 windowWidth;
    i32 windowHeight;
};

///////////////////////////////////////////////////////////

struct RenderDataWire
{
    com::Array<VertexWire, VERTEX_COUNT_MAX_WIRE> vertices;
    void Clear();
};

///////////////////////////////////////////////////////////

void RenderDataWire::Clear()
{
    vertices.count = 0; 
}

