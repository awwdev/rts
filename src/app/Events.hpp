#pragma once

#include "com/Types.hpp"

///////////////////////////////////////////////////////////

namespace mini::app {

///////////////////////////////////////////////////////////

enum class EventEnum
{
    ExitSizeMove,
    KeyDown,
    KeyUp,
    ButtonDown,
    ButtonUp,
};

////////////////////////////////////////////////////////////

enum class KeyboardEnum
{
    Escape,
};

////////////////////////////////////////////////////////////

enum class MouseEnum
{
    Left,
    Right,
};

///////////////////////////////////////////////////////////

struct WindowData
{
    i32 width;
    i32 height;
};

////////////////////////////////////////////////////////////

struct KeyboardData
{
    KeyboardEnum type;
};

////////////////////////////////////////////////////////////

struct MouseData
{
    MouseEnum type;
    i32 xpos;
    i32 ypos;
};

///////////////////////////////////////////////////////////

struct Event
{
    EventEnum eventEnum;
    union
    {
        WindowData window;
        KeyboardData keyboard;
        MouseData mouse;
    };
};

///////////////////////////////////////////////////////////

}//ns